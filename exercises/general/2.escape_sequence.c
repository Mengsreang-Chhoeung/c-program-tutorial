#include<stdio.h>

int main() {
	// using \t for tab and \n for new line
	printf("Hello\tWorld\n");
	// using "" and ''
	printf("What\'s your name? My name is \"Kok\".");
	// using \a for have sound alarm or beep
	printf("\nHello World\a");
	// using \b for move cursor from right to left
	printf("\nHello World\b\b\b");
	getch();
	return 0;
}
